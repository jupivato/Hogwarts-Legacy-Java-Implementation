package br.edu.unifei.ecot12.projetoFinal.HogwartsLegacy;

public class Broomstick 
{
	private String name;
	private String wood;
	private float resistance;
	private int seriesNumber;
	
	//get set
	public String getName() {return name;}
	public void setName(String name) {this.name = name;}
	public String getWood() {return wood;}
	public void setWood(String wood) {this.wood = wood;}
	public float getResistance() {return resistance;}
	public void setResistance(float resistance) {this.resistance = resistance;}
	public int getSeriesNumber() {return seriesNumber;}
	public void setSeriesNumber(int seriesNumber) {this.seriesNumber = seriesNumber;}
}
